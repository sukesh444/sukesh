#include <stdio.h>   // Preprocessor directive: includes the standard input-output library

// main() function: entry point of every C program
int main() {
    // Printing name using printf
    printf("Name: Alex\n");     // \n ensures output goes to a new line
    
    // Printing age using printf
    printf("Age: 20\n");        // Again, \n moves to the next line
    
    // Printing country using printf
    printf("Country: India\n"); // Each detail is printed on a separate line
    
    return 0;   // Indicates successful program termination
}
